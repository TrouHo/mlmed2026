# 3) Patch crop
def crop_patch(vol_zyx, center_xyz, patch_size=64):
    cx, cy, cz = center_xyz
    r = patch_size // 2
    zc, yc, xc = cz, cy, cx  # convert to z,y,x

    z1, z2 = zc - r, zc + r
    y1, y2 = yc - r, yc + r
    x1, x2 = xc - r, xc + r

    pad_z1 = max(0, -z1); pad_y1 = max(0, -y1); pad_x1 = max(0, -x1)
    pad_z2 = max(0, z2 - vol_zyx.shape[0]); pad_y2 = max(0, y2 - vol_zyx.shape[1]); pad_x2 = max(0, x2 - vol_zyx.shape[2])

    z1 = max(0, z1); y1 = max(0, y1); x1 = max(0, x1)
    z2 = min(vol_zyx.shape[0], z2); y2 = min(vol_zyx.shape[1], y2); x2 = min(vol_zyx.shape[2], x2)

    patch = vol_zyx[z1:z2, y1:y2, x1:x2]
    if any([pad_z1, pad_z2, pad_y1, pad_y2, pad_x1, pad_x2]):
        patch = np.pad(patch,
                       ((pad_z1,pad_z2),(pad_y1,pad_y2),(pad_x1,pad_x2)),
                       mode="constant",
                       constant_values=0.0)
    assert patch.shape == (patch_size, patch_size, patch_size)

    offset = (x1 - pad_x1, y1 - pad_y1, z1 - pad_z1)  # virtual patch origin in global coords
    return patch, offset

def far_from_gt(center_xyz, gt_centers, min_dist_vox=16):
    cx,cy,cz = center_xyz
    for (gx,gy,gz) in gt_centers:
        if (cx-gx)**2 + (cy-gy)**2 + (cz-gz)**2 < (min_dist_vox**2):
            return False
    return True

