@torch.no_grad()
def whole_scan_grid_detect_lung(model, vol_norm_zyx, lung_mask_zyx,
                               patch_size=64, step=32, topk=10, batch_size=16):
    model.eval()
    D,H,W = vol_norm_zyx.shape
    r = patch_size//2

    xs = list(range(r, W-r, step))
    ys = list(range(r, H-r, step))
    zs = list(range(r, D-r, step))

    centers=[]
    for z in zs:
        for y in ys:
            for x in xs:
                if lung_mask_zyx[z,y,x]:
                    centers.append((x,y,z))

    print("grid centers in lung:", len(centers))

    results=[]
    for i in range(0, len(centers), batch_size):
        batch_centers = centers[i:i+batch_size]
        patches=[]
        for (x,y,z) in batch_centers:
            patch,_ = crop_patch(vol_norm_zyx, (x,y,z), patch_size)
            patches.append(patch)
        xb = torch.from_numpy(np.stack(patches)[:,None].astype(np.float32)).to(device)
        p, off = model(xb)

        p = p.squeeze(1).detach().cpu().numpy()
        off = off.detach().cpu().numpy()

        for j,(gcx,gcy,gcz) in enumerate(batch_centers):
            dx,dy,dz = off[j]
            px = float(np.clip(gcx + dx*r, 0, W-1))
            py = float(np.clip(gcy + dy*r, 0, H-1))
            pz = float(np.clip(gcz + dz*r, 0, D-1))
            results.append({
                "p": float(p[j]),
                "pred_center_xyz": (px,py,pz),
                "grid_center_xyz": (gcx,gcy,gcz),
                "pred_off": (float(dx),float(dy),float(dz))
            })

    results.sort(key=lambda d: d["p"], reverse=True)
    return results[:topk]

