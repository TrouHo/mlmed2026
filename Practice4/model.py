# 1) Rough lung mask (thô, đủ để lọc gan/xương)
def rough_lung_mask(vol_raw_zyx):
    mask = vol_raw_zyx < -400
    st = generate_binary_structure(3, 1)
    mask = binary_opening(mask, structure=st, iterations=1)
    mask = binary_closing(mask, structure=st, iterations=2)
    lbl, n = label(mask)
    if n == 0:
        return mask
    counts = np.bincount(lbl.ravel()); counts[0] = 0
    keep = counts.argsort()[-2:]   # top2 components
    return np.isin(lbl, keep)

