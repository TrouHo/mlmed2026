import random

class LunaRegDatasetHardNeg(Dataset):
    def __init__(self, df_use, patch_size=64, neg_ratio=2.0, seed=0, min_dist_vox=16):
        self.df = df_use.reset_index(drop=True)
        self.patch_size = patch_size
        self.neg_ratio = float(neg_ratio)
        self.min_dist_vox = int(min_dist_vox)
        self.rng = random.Random(seed)

        self.pos_items = list(range(len(self.df)))
        self.n_neg = int(len(self.pos_items) * self.neg_ratio)
        self.all_sids = sorted(list(mhd_index.keys()))

    def __len__(self):
        return len(self.pos_items) + self.n_neg

    def __getitem__(self, idx):
        is_pos = idx < len(self.pos_items)
        r = self.patch_size // 2

        if is_pos:
            row = self.df.iloc[idx]
            sid = row["seriesuid"]
            img, vol_raw = load_scan(sid)
            vol = normalize_ct(vol_raw)

            cx, cy, cz = world_to_index(img, row["coordX"], row["coordY"], row["coordZ"])
            patch, offset = crop_patch(vol, (cx,cy,cz), self.patch_size)

            px = cx - offset[0]; py = cy - offset[1]; pz = cz - offset[2]
            cx0, cy0, cz0 = r, r, r

            dx = (px - cx0) / r
            dy = (py - cy0) / r
            dz = (pz - cz0) / r

            cls = 1.0
            mask = 1.0
            off = np.array([dx,dy,dz], dtype=np.float32)

        else:
            sid = self.rng.choice(self.all_sids)
            img, vol_raw = load_scan(sid)
            vol = normalize_ct(vol_raw)

            lung = lung_cache[sid]
            gt = gt_center_cache.get(sid, [])

            D,H,W = lung.shape
            ok = False
            for _ in range(300):
                z = self.rng.randint(r, D-r-1)
                y = self.rng.randint(r, H-r-1)
                x = self.rng.randint(r, W-r-1)
                if lung[z,y,x] and far_from_gt((x,y,z), gt, min_dist_vox=self.min_dist_vox):
                    ok = True
                    cx,cy,cz = x,y,z
                    break
            if not ok:
                cx = self.rng.randint(r, W-r-1)
                cy = self.rng.randint(r, H-r-1)
                cz = self.rng.randint(r, D-r-1)

            patch, _ = crop_patch(vol, (cx,cy,cz), self.patch_size)
            cls = 0.0
            mask = 0.0
            off = np.zeros(3, dtype=np.float32)

        x = torch.from_numpy(patch[None,...].astype(np.float32))  # (1,D,H,W)
        cls = torch.tensor([cls], dtype=torch.float32)           # (1,)
        off = torch.from_numpy(off)                              # (3,)
        mask = torch.tensor([mask], dtype=torch.float32)         # (1,)
        return x, cls, off, mask

# 6) Model
class Tiny3DDet(nn.Module):
    def __init__(self, base=16):
        super().__init__()
        self.feat = nn.Sequential(
            nn.Conv3d(1, base, 3, padding=1), nn.InstanceNorm3d(base), nn.ReLU(inplace=True),
            nn.MaxPool3d(2),

            nn.Conv3d(base, base*2, 3, padding=1), nn.InstanceNorm3d(base*2), nn.ReLU(inplace=True),
            nn.MaxPool3d(2),

            nn.Conv3d(base*2, base*4, 3, padding=1), nn.InstanceNorm3d(base*4), nn.ReLU(inplace=True),
            nn.AdaptiveAvgPool3d(1),
        )
        self.head_cls = nn.Linear(base*4, 1)
        self.head_reg = nn.Linear(base*4, 3)

    def forward(self, x):
        f = self.feat(x).flatten(1)
        p = torch.sigmoid(self.head_cls(f))   # (B,1)
        off = torch.tanh(self.head_reg(f))    # (B,3)
        return p, off

def train_epoch_det(model, loader, optim, lam_reg=1.0):
    model.train()
    tot = 0.0
    for x, cls, off, mask in tqdm(loader, leave=False):
        x = x.to(device)
        cls = cls.to(device)
        off = off.to(device)
        mask = mask.to(device)

        p, pred_off = model(x)
        loss_cls = F.binary_cross_entropy(p, cls)
        reg = F.smooth_l1_loss(pred_off, off, reduction="none").mean(dim=1, keepdim=True)
        loss_reg = (reg * mask).sum() / (mask.sum() + 1e-8)
        loss = loss_cls + lam_reg * loss_reg

        optim.zero_grad()
        loss.backward()
        optim.step()
        tot += loss.item() * x.size(0)
    return tot / len(loader.dataset)

@torch.no_grad()
def eval_epoch_det(model, loader, lam_reg=1.0):
    model.eval()
    tot = 0.0
    for x, cls, off, mask in loader:
        x = x.to(device); cls = cls.to(device); off = off.to(device); mask = mask.to(device)
        p, pred_off = model(x)
        loss_cls = F.binary_cross_entropy(p, cls)
        reg = F.smooth_l1_loss(pred_off, off, reduction="none").mean(dim=1, keepdim=True)
        loss_reg = (reg * mask).sum() / (mask.sum() + 1e-8)
        loss = loss_cls + lam_reg * loss_reg
        tot += loss.item() * x.size(0)
    return tot / len(loader.dataset)

